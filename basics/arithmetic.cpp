#include <iostream>

using namespace std;

int main()
{
  cout << "8/10: " << 8 / 10 << endl;
  cout << "13/10: " << 13 / 10 << endl;

  cout << "0/2: " << 0 / 2 << endl;
  cout << "1/2: " << 1 / 2 << endl;

  // mod
  cout << "3%0: " << 3 % 0 << endl;
  cout << "3%1: " << 3 % 1 << endl;
  cout << "3%2: " << 3 % 2 << endl;
}